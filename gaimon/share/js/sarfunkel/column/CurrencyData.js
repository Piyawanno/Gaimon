class CurrencyData{
	constructor(origin, exchanged, originCurrency, exchangedCurrency, rate, exchangeDate){
		this.origin = origin;
		this.exchanged = exchanged;
		this.originCurrency = originCurrency;
		this.exchangedCurrency = exchangedCurrency;
		this.rate = rate;
		this.exchangeDate = exchangeDate;
	}
}